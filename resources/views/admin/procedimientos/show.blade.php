    <div class="contenedor1">
        <p class="card-description">
       <p>La otoplastía o cirugía de las orejas es un procedimiento destinado a modificar la forma o corregir la posición de las orejas en relación con la cabeza (orejas separadas). Puede realizarse en niños desde los cuatro años de edad, aunque también se lleva a cabo en adultos.

        <p> Otros procedimientos que pueden corregirse con esta cirugía son:</p>
        
       <p> Orejas "caídas", en las que la parte superior parece que se dobla hacia adelante. También se pueden mejorar lóbulos demasiado grandes o estrechos, así como los que tienen demasiados pliegues o arrugas.
       </p>
         
        Se pueden incluso reconstruir nuevas orejas para aquellas personas que las perdieron en accidentes o que hayan nacido sin ellas (microtia o anotia).</p>
