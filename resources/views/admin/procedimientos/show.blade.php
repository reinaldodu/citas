    <center style="font-size: 20px; background-color: #1A355B; color: white;">Preguntas frecuentes
    </center>
<br>

<p style="font-weight: bold; color:#165084">1-¿En qué consiste la cirugía?</p>
        <p>Se realiza una incisión por la parte posterior de la oreja y retira la piel para ver el cartílago auricular. Se realizan cortes específicos al cartílago para luego moldearlo a la posición requerida, se colocan puntos de sutura para mantener la nueva forma de la oreja. En algunos casos el cirujano si considera necesario eliminará una porción del cartílago para obtener el resultado deseado.</p>


<p style="font-weight: bold; color:#165084">2- ¿Cuánto tiempo dura la cirugía?</p>
        <p> La cirugía se realiza generalmente en un lapso de 1 a 2 horas, aunque en casos más complicados puede llevarse más tiempo.
            
             </p>

<p style="font-weight: bold; color:#165084">3- ¿Es necesaria la hospitalización?</p>
        <p> No. Puede ser un procedimiento de tipo ambulatorio. Su médico lo decidirá junto con usted según sus requerimientos y su estado de salud, pero siempre en un lugar seguro. En el caso de pacientes menores su cirujano puede recomendarle que se realice el procedimiento bajo anestesia general. Para los mayores o los adultos puede ser una opción la anestesia local o una combinación entre local y sedación permaneciendo despiertos y relajados.</p>


<p style="font-weight: bold; color:#165084">4- ¿En qué consiste el proceso postoperatorio?</p>
        <p> Tanto niños como adultos vuelven a sus actividades normales después en un lapso de tres a cinco días después de la cirugía. Se prescriben analgésicos adecuados para controlar el dolor. Es probable que presente sensación de hormigueo y calor durante los siguientes días a la cirugía. Su médico le dará las
            
            instrucciones postoperatorias específicas y los cuidados que debe tener en el postoperatorio.</p>
        <p><i>La mayoría de los puntos se retiran en un plazo de 7 a 10 días después de la operación.</i></p>

<p style="font-weight: bold; color:#165084">5- ¿Qué cuidados postoperatorios debo tener?</p>
         <p>Después de la cirugía se recomienda reposo parcial en casa, mantener la cabeza más elevada con ayuda de dos almohadas (en un ángulo de 30 grados) por varios días después de la cirugía, para evitar así la inflamación. Debe
            
            evitar el sol y el calor. Puede presentar una sensación de adormecimiento, esto es totalmente normal y desaparece en pocas semanas o meses.
<p><i>Es recomendable usar una banda elástica o balerina liviana para la cabeza por dos a tres semanas para ayudar al proceso de cicatrización y proteger las orejas.</i></p>

<p style="font-weight: bold; color:#165084">6- ¿En cuánto tiempo veo el resultado final de la cirugía?</p>
        <p>Aunque el resultado es notorio inmediatamente después de la cirugía, por el
            
            proceso inflamatorio natural pueden pasar varias semanas hasta que la inflamación desaparezca por completo y hasta 18 meses para que las cicatrices sean casi imperceptibles, sin embargo se debe considerar el tipo de cicatrización de cada paciente (cicatrices patológicas).</p>
